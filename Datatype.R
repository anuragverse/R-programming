#1. Numeric Operations
num1 <- 10.5
num2 <- 5
sum <- num1 + num2
print(sum)

#2. Integer Operations
int1 <- as.integer(10)
int2 <- as.integer(3)
modulus <- int1 %% int2  # Modulo operation
print(modulus)

#3. Character Operations
char1 <- "Hello"
char2 <- "World"
concat_str <- paste(char1, char2)  # String concatenation
print(concat_str)

#4. Logical Operations
log1 <- TRUE
log2 <- FALSE-
log_result <- log1 & log2  # Logical AND operation
print(log_result)

log_result_or <- log1 | log2   # Logical OR
print(log_result_or)

#5. Complex Number Operations
comp1 <- 2 + 3i
comp2 <- 1 + 2i
comp_sum <- comp1 + comp2  # Complex number addition
print(comp_sum)

#6. Raw Data Type Operations
raw_data <- charToRaw("R Programming")
print(raw_data)